      Between the bun we find several goods and services such as food prices,
      labor, energy, transportation and more.
      Due to its global standard, it can be used to gauge purchasing power and
      inflation.
      <br />
      <br />

      The Big Mac Index was invented by <i>The Economist</i> in 1986.

      It consists of the price of the burger in various countries that are
      converted to the US dollar.
      <br />
      <br />

